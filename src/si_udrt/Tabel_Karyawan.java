    public void UpdateTabel() 
    {
        try 
        {
            String sql = "SELECT * FROM tabel_karyawan;";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            String [] data = new String[5];
            int i = 1;
       
            while(rs.next())
               {
                  data[0] = rs.getString("id_karyawan");
                  data[1] = rs.getString("nama_karyawan");
                  data[2] = rs.getString("hp_karyawan");
                  data[3] = rs.getString("alamat_karyawan");
                  data[4] = rs.getString("status_karyawan");
                  dtm.addRow(data);
                  i++;
                }
        }
            catch (Exception e)
            {
               JOptionPane.showMessageDialog(null, e);
            }
    }
    
